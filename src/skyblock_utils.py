def dungeon_class_id_to_name(id: str) -> str:
    if id == 'tank':
        return '坦克'
    elif id == 'healer':
        return '治疗师'
    elif id == 'berserk':
        return '狂战士'
    elif id == 'mage':
        return '法师'
    elif id == 'archer':
        return '射手'
    return id
